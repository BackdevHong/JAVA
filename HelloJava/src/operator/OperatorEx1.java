package operator;

public class OperatorEx1 {

	public static void main(String[] args) {

		int num = -10;
		
		int num2 = 20;
		
		System.out.println(+num);
		System.out.println(+num2);
		
		System.out.println(-num);
		System.out.println(-num2);

		System.out.println(num);
		System.out.println(num2);

	}

}
